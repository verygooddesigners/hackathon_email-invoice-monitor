def _run_monitor():
    """Run the invoice monitoring loop in a background thread."""
    global monitor_running
    monitor_running = True
    stop_event.clear()

    try:
        # Ensure logs directory exists
        os.makedirs(BASE_DIR / "logs", exist_ok=True)

        # Set up logging with our queue handler
        logger = logging.getLogger("invoice_monitor")
        logger.setLevel(logging.INFO)

        # File handler
        fh = logging.FileHandler(BASE_DIR / "logs" / "invoice_monitor.log")
        fh.setFormatter(logging.Formatter("%(asctime)s [%(levelname)s] %(name)s: %(message)s"))
        logger.addHandler(fh)

        # Queue handler for live UI
        qh = QueueLogHandler()
        qh.setFormatter(logging.Formatter("%(asctime)s [%(levelname)s] %(message)s"))
        logger.addHandler(qh)

        # Console handler
        ch = logging.StreamHandler()
        ch.setFormatter(logging.Formatter("%(asctime)s [%(levelname)s] %(message)s"))
        logger.addHandler(ch)

        # Change to project directory so relative paths work
        os.chdir(BASE_DIR)

        # Force re-read of .env
        from dotenv import load_dotenv
        load_dotenv(ENV_PATH, override=True)

        # Re-import config to pick up fresh env vars
        if "config" in sys.modules:
            del sys.modules["config"]
        import config as cfg

        from services.gmail_service import GmailService
        from services.sheets_service import SheetsService
        from services.extractor import extract_invoice_data
        from services.validator import validate_invoice
        from services.notifier import send_discrepancy_alerts
        from parsers.email_body_parser import extract_email_body
        from parsers.docx_parser import extract_docx_text
        from parsers.excel_parser import extract_excel_text

        gmail = GmailService()
        sheets = SheetsService()

        logger.info("=" * 50)
        logger.info("  RotoMath started via Control Panel")
        logger.info(f"  Inbox 1: {cfg.INBOX_1_EMAIL}")
        logger.info(f"  Inbox 2: {cfg.INBOX_2_EMAIL}")
        logger.info(f"  Poll interval: {cfg.POLL_INTERVAL_MINUTES} minutes")
        logger.info("=" * 50)

        while not stop_event.is_set():
            logger.info(f"=== Monitoring cycle: {datetime.now().isoformat()} ===")

            sheet_ids = sheets.ensure_spreadsheets_exist()

            for account in ["invoice", "kevin"]:
                if stop_event.is_set():
                    break
                try:
                    sheet_id = sheet_ids.get(account, "")
                    if not sheet_id:
                        logger.error(f"No sheet ID for {account} — skipping")
                        continue

                    logger.info(f"--- Processing inbox: {account} ---")
                    messages = gmail.get_unread_messages(account)
                    if not messages:
                        continue

                    for message in messages:
                        if stop_event.is_set():
                            break

                        message_id = message["id"]
                        subject = gmail.get_message_subject(message)
                        logger.info(f"Processing: {subject} (ID: {message_id})")

                        try:
                            body_text = extract_email_body(message.get("payload", {}))

                            attachment_texts = []
                            attachment_name = ""
                            attachments = gmail.get_attachments(account, message)

                            for att in attachments:
                                filename = att["filename"]
                                data = att["data"]
                                ext = os.path.splitext(filename)[1].lower()

                                if ext in (".docx", ".doc"):
                                    parsed = extract_docx_text(data)
                                elif ext in (".xlsx", ".xls"):
                                    parsed = extract_excel_text(data)
                                else:
                                    continue

                                if parsed and not parsed.startswith("[ERROR]"):
                                    attachment_texts.append(parsed)
                                    attachment_name = filename

                            combined_text = body_text
                            if attachment_texts:
                                combined_text += "\n\n--- ATTACHMENT ---\n\n" + "\n\n".join(attachment_texts)

                            if not combined_text.strip():
                                gmail.mark_as_read(account, message_id)
                                continue

                            invoice = extract_invoice_data(
                                raw_text=combined_text,
                                source_email=cfg.INBOX_1_EMAIL if account == "invoice" else cfg.INBOX_2_EMAIL,
                                attachment_name=attachment_name,
                            )

                            if not invoice:
                                gmail.mark_as_read(account, message_id)
                                continue

                            invoice = validate_invoice(invoice)
                            sheets.append_invoice(invoice, sheet_id, message_id=message_id)

                            if invoice.has_discrepancy:
                                send_discrepancy_alerts(invoice, gmail, account, message)
                                record_alert(invoice)
                                logger.warning(
                                    f"ALERT: Discrepancy for {invoice.freelancer_name} "
                                    f"(${invoice.discrepancy_amount:.2f})"
                                )

                            gmail.mark_as_read(account, message_id)
                            status = "DISCREPANCY" if invoice.has_discrepancy else "OK"
                            logger.info(f"Done: {invoice.freelancer_name} — ${invoice.stated_total:.2f} — {status}")

                        except Exception as e:
                            logger.error(f"Error processing {message_id}: {e}", exc_info=True)
                            gmail.mark_as_read(account, message_id)

                except Exception as e:
                    logger.error(f"Error processing {account} inbox: {e}", exc_info=True)

            logger.info("=== Cycle complete ===")

            # Wait for next cycle or stop signal
            stop_event.wait(timeout=cfg.POLL_INTERVAL_MINUTES * 60)

    except Exception as e:
        log_queue.put({
            "type": "error",
            "timestamp": datetime.now().isoformat(),
            "level": "ERROR",
            "message": f"Monitor crashed: {e}",
        })
    finally:
        monitor_running = False
        log_queue.put({
            "type": "status",
            "timestamp": datetime.now().isoformat(),
            "level": "INFO",
            "message": "Monitor stopped.",
        })


