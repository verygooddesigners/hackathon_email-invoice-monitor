def _read_env() -> dict:
    """Read the .env file into a dict."""
    env = {}
    if ENV_PATH.exists():
        for line in ENV_PATH.read_text().splitlines():
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            if "=" in line:
                k, v = line.split("=", 1)
                env[k.strip()] = v.strip()
    return env


def _write_env(env: dict):
    """Write a dict back to the .env file."""
    lines = []
    lines.append("# RotoMath configuration")
    lines.append("# Generated by Control Panel\n")
    for k, v in env.items():
        lines.append(f"{k}={v}")
    ENV_PATH.write_text("\n".join(lines) + "\n")


def _get_setup_status() -> dict:
    """Check what's configured and what's missing."""
    env = _read_env()
    creds_path = env.get("GOOGLE_CREDENTIALS_PATH", "credentials/google-credentials.json")
    creds_exists = (BASE_DIR / creds_path).exists()

    token_invoice = (BASE_DIR / "token" / "token-invoice.json").exists()
    token_kevin = (BASE_DIR / "token" / "token-kevin.json").exists()

    return {
        "anthropic_key": bool(env.get("ANTHROPIC_API_KEY", "").strip()),
        "google_creds_file": creds_exists,
        "inbox_1": env.get("INBOX_1_EMAIL", ""),
        "inbox_2": env.get("INBOX_2_EMAIL", ""),
        "token_invoice": token_invoice,
        "token_kevin": token_kevin,
        "sheet_invoice": bool(env.get("SHEET_ID_INVOICE", "").strip()),
        "sheet_kevin": bool(env.get("SHEET_ID_KEVIN", "").strip()),
        "poll_interval": env.get("POLL_INTERVAL_MINUTES", "5"),
        "all_required": (
            bool(env.get("ANTHROPIC_API_KEY", "").strip())
            and creds_exists
            and bool(env.get("INBOX_1_EMAIL", "").strip())
            and bool(env.get("INBOX_2_EMAIL", "").strip())
        ),
    }


