# ---------------------------------------------------------------------------
# Alert persistence
# ---------------------------------------------------------------------------

def _load_alerts() -> list[dict]:
    if ALERTS_PATH.exists():
        try:
            return json.loads(ALERTS_PATH.read_text())
        except (json.JSONDecodeError, OSError):
            return []
    return []


def _save_alerts(alerts: list[dict]):
    ALERTS_PATH.parent.mkdir(parents=True, exist_ok=True)
    ALERTS_PATH.write_text(json.dumps(alerts, indent=2))


def record_alert(invoice) -> None:
    """Called by the monitoring loop when a discrepancy is found."""
    alerts = _load_alerts()
    entry = {
        "timestamp": datetime.now().isoformat(),
        "freelancer": invoice.freelancer_name,
        "invoice_number": invoice.invoice_number or "N/A",
        "stated_total": invoice.stated_total,
        "calculated_total": invoice.calculated_total,
        "discrepancy_amount": invoice.discrepancy_amount,
        "line_items": invoice.line_items,
        "source_email": invoice.source_email,
        "attachment": invoice.attachment_name or "Email body",
    }
    alerts.insert(0, entry)  # newest first
    _save_alerts(alerts)

    # Push to live stream
    log_queue.put(entry)


# ---------------------------------------------------------------------------
# Custom log handler that pushes to SSE queue
# ---------------------------------------------------------------------------

class QueueLogHandler(logging.Handler):
    def emit(self, record):
        try:
            log_queue.put({
                "type": "log",
                "timestamp": datetime.now().isoformat(),
                "level": record.levelname,
                "message": self.format(record),
            })
        except Exception:
            pass


