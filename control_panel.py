@app.route("/api/save-config", methods=["POST"])
def api_save_config():
    data = request.json
    env = _read_env()

    if "anthropic_key" in data:
        env["ANTHROPIC_API_KEY"] = data["anthropic_key"]
    if "inbox_1" in data:
        env["INBOX_1_EMAIL"] = data["inbox_1"]
    if "inbox_2" in data:
        env["INBOX_2_EMAIL"] = data["inbox_2"]
    if "poll_interval" in data:
        env["POLL_INTERVAL_MINUTES"] = str(data["poll_interval"])

    # Ensure defaults
    env.setdefault("GOOGLE_CREDENTIALS_PATH", "credentials/google-credentials.json")
    env.setdefault("SHEET_ID_INVOICE", "")
    env.setdefault("SHEET_ID_KEVIN", "")

    _write_env(env)
    return jsonify({"ok": True})


@app.route("/api/start", methods=["POST"])
def api_start():
    global monitor_thread
    if monitor_running:
        return jsonify({"ok": False, "error": "Already running"})

    monitor_thread = threading.Thread(target=_run_monitor, daemon=True)
    monitor_thread.start()
    return jsonify({"ok": True})


@app.route("/api/stop", methods=["POST"])
def api_stop():
    if not monitor_running:
        return jsonify({"ok": False, "error": "Not running"})
    stop_event.set()
    return jsonify({"ok": True})


@app.route("/api/stream")
def api_stream():
    """Server-Sent Events endpoint for live log streaming."""
    def generate():
        while True:
            try:
                item = log_queue.get(timeout=30)
                yield f"data: {json.dumps(item)}\n\n"
            except queue.Empty:
                yield f"data: {json.dumps({'type': 'heartbeat'})}\n\n"

    return Response(generate(), mimetype="text/event-stream")


